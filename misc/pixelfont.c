               if ((s[1]&0xc0) != 0x80) break;
               *c = (i32)(s[0]&0x1f) << 6 |
                    (i32)(s[1]&0x3f) << 0;
               if (*c < 0x80) {
                   break;
               }
               return 2;
    case 0xe0: if (len < 3) break;
               if ((s[1]&0xc0) != 0x80) break;
               if ((s[2]&0xc0) != 0x80) break;
               *c = (i32)(s[0]&0x0f) << 12 |
                    (i32)(s[1]&0x3f) <<  6 |
                    (i32)(s[2]&0x3f) <<  0;
               if (*c<0x800 || (*c>=0xd800 && *c<=0xdfff)) {
                   break;
               }
               return 3;
    case 0xf0: if (len < 4) break;
               if ((s[1]&0xc0) != 0x80) break;
